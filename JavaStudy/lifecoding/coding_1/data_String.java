package coding_1;

public class data_String {
	public static void main(String[] args) {
		//
		System.out.println("Hello World"); // 문자열(String)" "  = 문자들이 모여서 열을 표현하는 데이터 타입
		System.out.println('H'); // 문자(Character) ' ' = 한 글자를 표현하는 데이터 타입
		System.out.println("H"); //큰따옴표 문자열 H
		
		//줄바꿈
		System.out.println("Hello " 
				+"Wolrd");
		System.out.println("Hello \nWorld"); // \n는 줄바꿈이다
		
		// escape
		System.out.println("Hello \"World\""); // \" 역슬래시 뒤에 있는 따옴표는 문자열의 큰 따옴표다
		
		// 낯선 분야를 공부할떄 작심삼일이 된다
		// 그런데 작심삼일이 왔을때 그렇게는 해석하지 않는다 = 의지가 박약해
		// 공부를 그만하라는 뇌의 명령이다
		// 우리가 배우는 수업은 하나하나가 그것 자체가 쓸모있는 목적지다
		// 언제든지 공부는 그만하고 일을 해라
		// 더 높은 곳으로 나아가기 위한 길목이다
		// 일을 잘하는 사람은 일만하지 않고 공부를 잘하는 사람은 공부만 하지 않는다
		// 일을 해야할때와 공부를 해야할때를 주체적으로 판단할수 있는 사람이 공부와 일을 다 잘하는 사람이다
	}
}
