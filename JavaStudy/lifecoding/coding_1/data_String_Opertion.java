package coding_1;

public class data_String_Opertion {

	public static void main(String[] args) {
		//문자열의 데이터 타입을 표현할수 있게 되면 문자열과 관련된 자바의 내장되어 있는 
		//여러가지 문자를 처리하는 굉장히 기능이 좋은것들을 사용하여 여러가지 문제를 해결할수 있다는 가능성을 보여주기 위해서 만든 수업이다 
		
		System.out.println("Hello World".length()); 
		// length() 문자열의 길이를 알려준다
		
		//이메일을 보내려는 사람의 이름을 바꿔야 한다하면 replace
		System.out.println("Hello. [[name]] ... bye. ".replace("[[name]]","doro"));
		// replace("기존에 있는 값 " , "바꿔야하는 값 " ); 
		
		
		
	}

}
